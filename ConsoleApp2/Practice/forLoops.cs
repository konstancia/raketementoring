namespace Practice;

public class forLoops
{

    static void Main(string[] args)
    {
        for (int i = 0; i < 5; i++)
        {
            
            Console.WriteLine("Go to the fridge");
            Console.WriteLine("open the door");
            Console.WriteLine("close door");
            Console.WriteLine("etc");
            Console.WriteLine($"I've done this task list for {i} times");
            
        }

    }

}
